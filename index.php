    function handleUserInput() {
        const userInput = document.getElementById('userInput').value.trim().toLowerCase();
        if (!userInput) return;
        
        displayMessage(userInput, 'user-message');
        
        let botResponse = '';
        switch (userInput) {
            case 'show home':
                botResponse = `<a href="https://litsedu.com/home" target="_blank">Home</a>`;
                break;
            case 'payment':
                botResponse = `<a href="https://litsedu.com/payment" target="_blank">Payment</a>`;
                break;
            case 'feedback':
                botResponse = `<a href="https://litsedu.com/feedback.html" target="_blank">Feedback</a>`;
                break;
            case 'contact us':
                botResponse = `<a href="https://litsedu.com/contact.html" target="_blank">Contact Us</a>`;
                break;
            case 'placement':
                botResponse = `<a href="https://litsedu.com/placement" target="_blank">Placement</a>`;
                break;
            default:
                botResponse = 'Sorry, I did not understand that. Please try again.';
        }

        displayMessage(botResponse, 'bot-message');
        document.getElementById('userInput').value = '';
    }

    function displayMessage(message, className) {
        const chatMessages = document.getElementById('chatMessages');
        const messageDiv = document.createElement('div');
        messageDiv.className = className;
        messageDiv.innerHTML = message;
        chatMessages.appendChild(messageDiv);
        chatMessages.scrollTop = chatMessages.scrollHeight;
    }

  

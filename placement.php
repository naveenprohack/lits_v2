    document.querySelector('.chatbot-container').style.display = 'none';
});

document.getElementById('sendBtn').addEventListener('click', function() {
    const userInput = document.getElementById('userInput');
    const userMessage = userInput.value.trim();

    if (userMessage) {
        const messageContainer = document.createElement('div');
        messageContainer.className = 'user-message';
        messageContainer.textContent = userMessage;
        document.querySelector('.chatbot-messages').appendChild(messageContainer);

        // Clear the input field
        userInput.value = '';

        // Auto-scroll to the bottom
        document.querySelector('.chatbot-messages').scrollTop = document.querySelector('.chatbot-messages').scrollHeight;

        // Add a bot response (for demonstration)
        setTimeout(() => {
            const botMessageContainer = document.createElement('div');
            botMessageContainer.className = 'bot-message';
            botMessageContainer.textContent = 'Thank you for your message! We will get back to you shortly.';
            document.querySelector('.chatbot-messages').appendChild(botMessageContainer);

            // Auto-scroll to the bottom
            document.querySelector('.chatbot-messages').scrollTop = document.querySelector('.chatbot-messages').scrollHeight;
        }, 1000);
    }
});

